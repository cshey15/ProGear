/**
 * LinkGearRequest Schema
 */
var LinkGearRequestSchema = new Schema({
    gear: { type: mongoose.Schema.Types.ObjectId, ref: 'Gear' },
    pro: { type: mongoose.Schema.Types.ObjectId, ref: 'Pro' },
    proofLink: {
        type: String,
        validate: [validateUrl, 'Proof link should be a valid url. Remember to include the http://'],
        required: 'Please provide proof'
    },
    explanation: {
        type: String,
        required: 'Please provide explanation',
    },
    status: {
        type: String, default: 'pending',
        enum: ['approved', 'denied', 'pending']
    },
    lastUpdated: {
        type: Date
    },
	created: {
		type: Date,
		default: Date.now
	},
    approver: {
        type: Schema.ObjectId,
        ref: 'User'
    },
	user: {
		type: Schema.ObjectId,
		ref: 'User'
	}
});

var options = {};
LinkGearRequestSchema.plugin(deepPopulate, options /* more on options below */);
mongoose.model('LinkGearRequest', LinkGearRequestSchema);